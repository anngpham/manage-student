package communityuni.com.model;

public class gv {
	private String name;
	private String id;
	public gv(String name, String id) {
		super();
		this.name = name;
		this.id = id;
	}
	public gv() {
    }
    public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	
}
