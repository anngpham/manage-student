package communityuni.com;

public class mh {
	private int id;
	private String name;
	private int idGv;
	public mh(int id, String name, int idGv) {
		super();
		this.id = id;
		this.name = name;
		this.idGv = idGv;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getIdGv() {
		return idGv;
	}
	public void setIdGv(int idGv) {
		this.idGv = idGv;
	}
	
}
